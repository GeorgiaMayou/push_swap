#include "libft.h"

size_t	ft_wrdlen(char const *s, int i, char c)
{	
	size_t	count;

	count = 0;
	while (s[i] == c)
		i++;
	while (s[i] !=c && s[i])
	{
		i++;
		count++;
	}
	return (count);
}
