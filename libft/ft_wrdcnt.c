#include "libft.h"

size_t   ft_wrdcnt(const char *s, char c)
{
    size_t  io;
    size_t  i;

    i = 0;
    io = 0;
    while (s[i])
    {
        while(s[i] == c)
            i++;
		if (s[i] != '\0')
			io++;
        while (s[i] != c && s[i])
            i++;
    }
    return (io);
}
